    private void init() {
        
            supps = new String[extra.countSuppliers()];
            setSuppliers();
            suppTable();
            setLocation(450, 180);
        
    }

    private void setSuppliers() {
        try {
            supps = client.getRemoteService().getSuppliers();
            for (String s : supps) {
                jComboBox1.addItem(s);
            }
        } catch (RemoteException ex) {
            Logger.getLogger(SelectSupplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void suppTable() {
        
            extra.getProductsValue(jTable1, "");
            model = (DefaultTableModel) jTable1.getModel();
            jTable1.setRowHeight(30);
            jTable1.setShowGrid(true);
            jTable1.setBackground(Color.WHITE);
            jTable1.setGridColor(Color.BLACK);
            jTable1.setSelectionBackground(Color.LIGHT_GRAY);
        

    }

